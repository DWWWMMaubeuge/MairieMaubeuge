                    <p> 
                        Maubeuge se situe dans le Hainaut, 
                        dans le sud-est du département du Nord, 
                        à environ: 33 km à l'est de Valenciennes, 55 km de Cambrai, 70 km au nord-est de Saint-Quentin, 75 km au sud-est de Lille et 200 km au nord-est de Paris à vol d'oiseau.

                        La frontière franco-belge n'est qu'à 7 km au nord de la ville. 

                        Le bassin houiller du Borinage est tout proche, bien qu'entièrement en territoire belge.

                        La ville belge de Mons, chef-lieu de la province de Hainaut, avec laquelle Maubeuge partage une politique culturelle commune, n'est qu'à 20 km. 

                        Maubeuge est également à 37 km de Charleroi et à 70 km de Namur et de Bruxelles.

                        La ville, baignée par la Sambre navigable, est une porte du parc naturel régional de l'Avesnois.

                        En termes de population, Maubeuge (29 944 habitants au 1er janvier 2017) est la 234e commune de France7 et la plus importante commune de l'arrondissement d'Avesnes-sur-Helpe.

                        La ville est au cœur de l'unité urbaine de Maubeuge qui rassemble 114 174 habitants en 2009, comprenant notamment les communes de Hautmont, Jeumont, Louvroil, Feignies, Rousies, Ferrière-la-Grande, Aulnoye-Aymeries et Recquignies entre autres. Il s'agit d'une agglomération transfrontalière puisque le tissu urbain est continu de part et d'autre de la frontière franco-belge, entre les villes de Jeumont (France) et d'Erquelinnes (Belgique).

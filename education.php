		<div class="stitarc">
			Apprendre dans les meilleures conditions
		</div>
		<div class="arc">
			<p class="test">
				Chaque jour, la ville accueille dans ses écoles, près de 3 168 élèves. Aussi, la ville compte près de 21 écoles publiques, dont 13 écoles maternelles et 12 écoles élémentaires. 

				Chaque année, la ville investit près de 1 500 euros par an et par élève, soit le double de la moyenne nationale, pour permettre à vos enfants d’évoluer dans des conditions optimales. 

				Depuis 2014, la municipalité a lancé le plan Ambition école 2020, qui prévoit la réhabilitation des écoles maubeugeoises avec plus de 7 millions d’euros investit d’içi à 2020. 

				En matière d’enseignement secondaire, la ville compte 5 collèges dont 1 privé, ainsi que 2 lycées généraux et 2 lycées professionnels. Du côté de l’enseignement supérieur, l’Université de Valenciennes et du Hainaut Cambresis mais aussi les différents lycées de la ville proposent aux étudiants un large panel de formation.
			</p>
		</div>
		<div class="stitarc">
			Restauration scolaire
		</div>
		<div class="arc">
			<p class="test">
				Dès 2016, la ville de Maubeuge a souhaité améliorer l’offre de restauration pour les enfants des écoles maubeugeoises.  La ville fait le choix de travailler avec un prestataire offrant des repas de qualité. 

				Au quotidien, priorité est donnée à l’utilisation de produit frais, de saison et si possible répondant aux critères de circuit court avec des produits locaux et régionaux. Un travail est également effectué pour favoriser le recours aux produits bio. 

				Ce sont donc chaque jour des repas équilibrés et diversifiés qui sont proposés de façon à respecter les besoins nutritionnels de vos enfants. 

				Une priorité a également été donnée en faveur de la diminution du gaspillage. Un nouveau système a été mis en place dans ce sens. Il est demandé aux familles de prévoir la réservation des repas au plus tard la veille, à l’arrivée en classe (le lundi pour le mardi, le mardi pour le jeudi, le jeudi pour le vendredi et le vendredi 
				pour le lundi), pour favoriser la préparation d’une quantité de repas, au plus près des besoins réels. 
			</p>
